public void ClosingAction(){
        String OutString="";
        if (SaveConfirm==false){
            OutString = Dialog[0];
            SavingAction();//Abre ventana para guardar los datos 
            if (SaveConfirm==true){
              OutString = Dialog[1];
            }//if saveconfirm==TRUE
        }//if 
        else{
            OutString = Dialog[1];
        }//else
        this.setAlwaysOnTop(false);
        int Opciones = JOptionPane.showConfirmDialog(null,OutString,"Salir de opciones",
                                                     JOptionPane.YES_NO_OPTION);
        if (Opciones == JOptionPane.YES_OPTION){
            this.dispose();
        }
        //Opciones == JOptionPane
    }//closingAction

public void SavingAction(){
      this.setAlwaysOnTop(false);
      int Opciones = JOptionPane.showConfirmDialog(null,"Â¿Quiere guardar la configuraciÃ³n del experimento?",
                                                     "Guardar ConfiguraciÃ³n",JOptionPane.YES_NO_OPTION);
        if (Opciones == JOptionPane.YES_OPTION){
            Preferences pref = Preferences.userNodeForPackage(GraficaCaida.class);
            pref.put("ExpName", ExpName.getText());
            pref.put("VariableX",VariableX_Name.getText());
            pref.put("VariableTypeX", TempComBox.getItemAt(TempComBox.getSelectedIndex()));
            pref.put("VariableIndexX", String.valueOf(TempComBox.getSelectedIndex()));
            pref.put("VariableY", VariableY_Name.getText());
            pref.put("VariableTypeY",TempType.getItemAt(TempType.getSelectedIndex()));
            pref.put("VariableIndexY", String.valueOf(TempType.getSelectedIndex()));
            SaveConfirm=true;
            SaveButton.setEnabled(false);
        }//Opciones == JOptionPane
}//SavingAction 
    
