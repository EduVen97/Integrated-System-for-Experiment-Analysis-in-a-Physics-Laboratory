    public OptionTIME(boolean LoadData) {
        Calendar calendar = Calendar.getInstance();
        DateFormat datef = new SimpleDateFormat("dd-MM-yyyy");
        String Today = datef.format(calendar.getTime());
        System.out.println(Today);
         initComponents();
       if (LoadData==true){
           try{
                this.setAlwaysOnTop(false);
                JOptionPane.showMessageDialog(null,"Cargando Datos de Experimento");
                Preferences pref = Preferences.userNodeForPackage(GraficaCaida.class);
                this.ExpName.setText(pref.get("ExpName", "Exp_Caida"));
                this.VariableX_Name.setText(pref.get("VariableX",VariableX_Name.getText()));
                if (VariableX_Name.getText().contentEquals("Temperatura")==true){
                    VariableX_Name.setText("Tiempo");
                    pref.put("VariableX","Tiempo");
                }
                this.TempComBox.setSelectedItem(pref.get("VariableTypeX", (String) TempComBox.getSelectedItem()));
                this.VariableY_Name.setText(pref.get("VariableY", VariableY_Name.getText()));
                this.TempType.setSelectedItem(pref.get("VariableTypeY", (String) TempType.getSelectedItem()));
           }
           catch (NullPointerException ex){
            System.out.println(ex);
            String Exp = ExpName.getText();
            Exp = Exp+"_Altura1_"+Today;
            ExpName.setText(Exp);
           }//NullPointer
        }//LoadData==true
        if (LoadData==false){
            String Exp = ExpName.getText();
            Exp = Exp+"_"+Today;
            ExpName.setText(Exp);
        }//if loadData-=false
        SaveButton.setEnabled(false);
    }//defaultOptionTemp
    
    
