    private void jMenuItem11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem11ActionPerformed
        // TODO add your handling code here:
        this.setAlwaysOnTop(false);
        int Message = JOptionPane.showConfirmDialog(null,"Desea cargar una configuraci贸n anterior de los "
                                                     + "experimentos?","Atenci贸n!",JOptionPane.YES_NO_OPTION,
                                                     JOptionPane.QUESTION_MESSAGE);
        if (Message == JOptionPane.YES_OPTION){
            Options = true;
        }//if 
        else if (Message==JOptionPane.NO_OPTION){
            Options = false;
        }//else if 
        OptionTIME opc = new OptionTIME();
        if (Options == true){
            opc = new OptionTIME(true);
        }
        opc.pack();
        opc.setLocationRelativeTo(null);
        opc.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        opc.setAlwaysOnTop(true);
        opc.setVisible(true);
    }//GEN-LAST:event_jMenuItem11ActionPerformed

